#include <stdio.h>
int main()
{
    int age = 22;
    printf("age is %d \n ", age); // "\n" is escape character and "%d" is format specifier for integer
    float pi = 3.14;
    printf("pi value is %f \n ", pi);
    char star = '*';
    printf("star is represented as %c \n", star);
    return 0;
}

// datatype         format specifier
// 1.Integer           %d
// 2.Float             %f
// 3.Character         %c